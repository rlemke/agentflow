def scaffold_test(spec: FacetSpec) -> str:
    """A pytest stub asserting the contract (replace the NotImplementedError check)."""
    sample = ", ".join(f'"{n}": ...' for n, _ in spec.params)
    return f'''"""Contract test for {spec.qualified_name} — SCAFFOLD."""

import pytest

from {spec.module_stem} import handle


def test_{spec.short_name.lower()}_not_yet_implemented():
    # Remove this once `handle` is implemented; then assert the real contract:
    #   result = handle({{{sample}}})
    #   assert result["{spec.returns[0][0] if spec.returns else "ok"}"] ...
    with pytest.raises(NotImplementedError):
        handle({{{sample}}})
'''


def review_checklist(spec: FacetSpec) -> str:
    """The human-facing steps to take the scaffold to a deployed handler."""
    return f"""# Review + deploy checklist — {spec.qualified_name}

This is NEW executable capability (arbitrary Python on the fleet), so it is a
reviewed code contribution to the handler package — not a catalog-only change.

1. [ ] Implement `handle()` in {spec.module_stem}.py (fill the body; put reusable
       logic in the package's `_<pkg>_tools/` per agent-spec/tools-pattern).
2. [ ] Add the `event facet` declaration to the package's FFL (see the .ffl scaffold),
       keeping param/return types in sync with the handler.
3. [ ] Wire registration: either keep this module's `register_handlers(runner)`, or
       add `{spec.short_name}` to the area's FACETS list + dispatch (match the
       package's existing pattern, e.g. road_handlers.py / filter_handlers.py).
4. [ ] Write the real test (replace the NotImplementedError stub).
5. [ ] `fw_validate` the package FFL (the new facet resolves) + run the tests.
6. [ ] Code review + commit to the handler package (the human gate for new code).
7. [ ] Deploy: `pip install -e <pkg>` + (re)start the runner so it registers the
       handler. Confirm with `scripts/list-runners` / the catalog preflight.
8. [ ] `scripts/catalog import-package <pkg>` to expose the facet in the catalog
       library, then author/validate/publish the workflow that uses it.
"""
